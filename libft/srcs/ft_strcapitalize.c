#include "libft.h"

char	*ft_strcapitalize(char *s)
{
	int		i;

	i = -1;
	while (s[++i])
	{
		if ('a' <= s[i] && s[i] <= 'z')
			s[i] -= 32;
	}
	return (s);
}
